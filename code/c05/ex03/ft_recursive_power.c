#include <stdio.h>

int	ft_recursive_power(int nb, int power)
{
	int	i;
	int	sum;

	i = 0;
	sum = 1;
	if (power < 0)
		return (0);
	if (power == 0 && nb == 0)
		return (1);
	if (power == 0)
		return (1);
	return (nb * ft_recursive_power(nb, power - 1));
}
